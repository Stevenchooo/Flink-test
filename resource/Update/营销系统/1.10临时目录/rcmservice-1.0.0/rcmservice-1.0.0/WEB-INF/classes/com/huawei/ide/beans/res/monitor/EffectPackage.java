package com.huawei.ide.beans.res.monitor;

/**
 * url package类
 * @author zWX301264
 *
 */
public class EffectPackage
{
    /**
     * 主键
     */
    private int id;
    
    /**
     * 名称
     */
    private String name;
    
    /**
     * category
     */
    private String category;
    
    /**
     * 获取id
     * @return id
     */
    public int getId()
    {
        return id;
    }
    
    /**
     * 设置id
     * @param id id
     */
    public void setId(int id)
    {
        this.id = id;
    }
    
    /**
     * 获取name 
     * @return name
     */
    public String getName()
    {
        return name;
    }
    
    /**
     * 设置name
     * @param name name
     */
    public void setName(String name)
    {
        this.name = name;
    }
    
    /**
     * 获取category
     * @return category
     */
    public String getCategory()
    {
        return category;
    }
    
    /**
     * 设置category
     * @param category category
     */
    public void setCategory(String category)
    {
        this.category = category;
    }
    
}
